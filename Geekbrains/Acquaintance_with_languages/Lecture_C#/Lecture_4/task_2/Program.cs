void FillImage(int row, int col) //Закрашиванье плюсами внутри изображения при помощи рекурсии.
{
    if(pic[row, col] == 0)
    {
        pic[row,col] = 1;
        FillImage(row-1, col);
        FillImage(row, col-1);
        FillImage(row+1, col);
        FillImage(row, col+1);
    }
}
void PrintImage(int[,]arg) // Печать в консоль контура изображения.
{
    for (int i = 0; i < arg.GetLength(0); i++)
    {
        for (int j = 0; j < arg.GetLength(1); j++)
        {
            if(arg[i,j] == 0)Console.Write(" ");
            else Console.Write("+");
        }
        Console.WriteLine();
    }
}
